#!/usr/bin/env python3
"""Prepare state-trajectory maps for the spike method benchmark.

This script creates a reproducible staging area for rendering a trajectory at
one dataset size. It does not run long jobs itself. It:

* symlinks GT maps for the requested states,
* symlinks cryoDRGN decoded maps when they already exist,
* recomputes RECOVAR no-reg zdim4 target points from the current embedding,
* writes an sbatch array to generate the missing RECOVAR compute_state maps,
* writes a separate CryoSPARC manifest for 3DFlex mean-latent generation.

The generated 3DFlex manifest deliberately points its bench_root at the
trajectory output directory, so it does not overwrite the normal 0/25/50
evaluation manifest.
"""

from __future__ import annotations

import argparse
import json
import os
import pickle
from pathlib import Path

import numpy as np


DEFAULT_SOURCE_ROOT = Path(
    "/scratch/gpfs/CRYOEM/gilleslab/tmp/"
    "spike_fullatom_consistency_grid256_noise3_b80_20260531"
)
DEFAULT_BENCH_ROOT = Path(
    "/scratch/gpfs/CRYOEM/gilleslab/tmp/"
    "spike_fullatom_method_sweep_noise3_b80_20260531"
)
DEFAULT_REBASE_REPO = Path(
    "/scratch/gpfs/GILLES/mg6942/recovar_dev/recovar_kernel_dev2_rebase"
)
DEFAULT_STATES = tuple(np.linspace(0, 99, 21, dtype=int).tolist())


def n_label(n_images: int) -> str:
    return f"n{n_images:08d}"


def symlink_replace(src: Path, dst: Path) -> None:
    if not src.exists():
        raise FileNotFoundError(src)
    dst.parent.mkdir(parents=True, exist_ok=True)
    if dst.is_symlink() or dst.exists():
        dst.unlink()
    os.symlink(src, dst)


def write_state_targets(
    *,
    source_run: Path,
    pipeline_dir: Path,
    states: tuple[int, ...],
    target_dir: Path,
) -> dict[str, object]:
    params_path = pipeline_dir / "model" / "params.pkl"
    coords_path = pipeline_dir / "model" / "zdim_4" / "latent_coords_noreg.npy"
    precision_path = pipeline_dir / "model" / "zdim_4" / "latent_precision_noreg.npy"
    state_path = source_run / "03_dataset" / "state_assignment.npy"

    for path in (params_path, coords_path, precision_path, state_path):
        if not path.exists():
            raise FileNotFoundError(path)

    with params_path.open("rb") as handle:
        params = pickle.load(handle)
    if isinstance(params, dict) and params.get("is_oracle_pipeline_output", False):
        raise RuntimeError(f"Pipeline is marked as oracle-generated: {pipeline_dir}")

    assignments = np.load(state_path).astype(np.int64).reshape(-1)
    coords = np.load(coords_path).astype(np.float64)
    if coords.shape[0] != assignments.size:
        raise RuntimeError(
            f"Embedding/state length mismatch: {coords.shape[0]} vs {assignments.size}"
        )

    target_dir.mkdir(parents=True, exist_ok=True)
    records: list[dict[str, object]] = []
    for state in states:
        mask = assignments == state
        if not np.any(mask):
            raise RuntimeError(f"No particles assigned to GT state {state}")
        target = np.mean(coords[mask], axis=0, dtype=np.float64).astype(np.float32)
        target_path = target_dir / f"state{state:04d}.txt"
        np.savetxt(target_path, target.reshape(1, -1))
        record = {
            "state": int(state),
            "n_particles": int(mask.sum()),
            "target_path": str(target_path),
            "target_point": target.astype(float).tolist(),
        }
        (target_dir / f"state{state:04d}.json").write_text(
            json.dumps(record, indent=2) + "\n"
        )
        records.append(record)

    manifest = {
        "source_run": str(source_run),
        "pipeline_dir": str(pipeline_dir),
        "coords": str(coords_path),
        "precision": str(precision_path),
        "regularization": "noreg",
        "zdim": 4,
        "states": list(states),
        "targets": records,
    }
    (target_dir / "manifest.json").write_text(json.dumps(manifest, indent=2) + "\n")
    return manifest


def write_recovar_sbatch(
    *,
    path: Path,
    rebase_repo: Path,
    source_run: Path,
    pipeline_dir: Path,
    target_dir: Path,
    output_dir: Path,
    states: tuple[int, ...],
) -> None:
    state_values = " ".join(str(x) for x in states)
    script = f"""#!/usr/bin/env bash
# Auto-generated by prepare_spike_trajectory_state_maps.py.
# RECOVAR zdim4 no-reg compute_state maps for a state trajectory.

#SBATCH --job-name=spike-traj-recovar
#SBATCH --partition=cryoem
#SBATCH --account=gilles
#SBATCH --gres=gpu:h100:1
#SBATCH --cpus-per-task=16
#SBATCH --mem=900G
#SBATCH --time=24:00:00
#SBATCH --array=0-{len(states) - 1}%4
#SBATCH --output={output_dir}/slurm/%x-%A_%a.out
#SBATCH --error={output_dir}/slurm/%x-%A_%a.err

set -euo pipefail

STATES=({state_values})
STATE="${{STATES[$SLURM_ARRAY_TASK_ID]}}"
STATE4="$(printf '%04d' "$STATE")"

WORKDIR="{rebase_repo}"
SOURCE_RUN="{source_run}"
PIPELINE_DIR="{pipeline_dir}"
TARGET_DIR="{target_dir}"
OUT_DIR="{output_dir}/recovar/compute_state/state${{STATE4}}"

AGENT_ID="spike-traj-recovar-${{SLURM_ARRAY_JOB_ID:-manual}}-${{SLURM_ARRAY_TASK_ID:-0}}"
export TMPDIR="/scratch/gpfs/GILLES/mg6942/tmp/${{AGENT_ID}}"
export PIXI_HOME="/scratch/gpfs/GILLES/mg6942/pixi_home/${{AGENT_ID}}"
export RATTLER_CACHE_DIR="/scratch/gpfs/GILLES/mg6942/rattler_cache/${{AGENT_ID}}"
mkdir -p "$TMPDIR" "$PIXI_HOME" "$RATTLER_CACHE_DIR" "{output_dir}/slurm" "$OUT_DIR"

unset PYTHONPATH PYTHONHOME CONDA_PREFIX VIRTUAL_ENV
export PYTHONNOUSERSITE=1
export XLA_PYTHON_CLIENT_PREALLOCATE=false
export XLA_PYTHON_CLIENT_MEM_FRACTION=.95
export TF_GPU_ALLOCATOR=cuda_malloc_async
export RECOVAR_CUDA_LIB="$WORKDIR/recovar/cuda/libcuda_backproject.so"

cd "$WORKDIR"
source /usr/share/Modules/init/bash 2>/dev/null || true
module load cudatoolkit/13.2 >/dev/null 2>&1 || true

PIXI_PY="$WORKDIR/.pixi/envs/default/bin/python"
if [[ ! -x "$PIXI_PY" ]]; then
  echo "Missing pixi Python: $PIXI_PY" >&2
  exit 2
fi
if [[ ! -f "$RECOVAR_CUDA_LIB" ]]; then
  PYTHON="$PIXI_PY" make -C recovar/cuda clean all
fi

echo "START=$(date -Is)"
echo "STATE=$STATE"
echo "SOURCE_RUN=$SOURCE_RUN"
echo "PIPELINE_DIR=$PIPELINE_DIR"
echo "OUT_DIR=$OUT_DIR"
echo "COMMIT=$(git rev-parse HEAD)"
echo "BRANCH=$(git rev-parse --abbrev-ref HEAD)"
nvidia-smi || true

"$PIXI_PY" - <<'PY'
from pathlib import Path
import jax
import recovar
repo = Path.cwd().resolve()
recovar_file = Path(recovar.__file__).resolve()
jax_file = Path(jax.__file__).resolve()
print("recovar", recovar_file, flush=True)
print("jax", jax_file, flush=True)
print("devices", jax.devices(), flush=True)
assert str(recovar_file).startswith(str(repo) + "/")
assert ".pixi/envs/default/" in str(jax_file)
if not any(getattr(device, "platform", "") == "gpu" for device in jax.devices()):
    raise RuntimeError("JAX did not see a GPU")
PY

if [[ -f "$OUT_DIR/state000.mrc" && "${{OVERWRITE_COMPUTE_STATE:-0}}" != "1" ]]; then
  echo "Reusing existing compute_state output: $OUT_DIR/state000.mrc"
else
  rm -rf "$OUT_DIR"
  mkdir -p "$OUT_DIR"
  cmd=(
    "$PIXI_PY" -m recovar.commands.compute_state
    "$PIPELINE_DIR"
    -o "$OUT_DIR"
    --latent-points "$TARGET_DIR/state${{STATE4}}.txt"
    --Bfactor 0
    --n-bins 50
    --maskrad-fraction 20
    --n-min-particles 100
    --save-all-estimates
    --kernel-regression-mode standard
    --embedding-option cov_dist
    --no-z-regularization
    --lazy
  )
  echo "COMPUTE_STATE_COMMAND=${{cmd[*]}}"
  /usr/bin/time -v "${{cmd[@]}}"
fi

echo "DONE=$(date -Is)"
echo "RECOVAR_MAP=$OUT_DIR/state000.mrc"
"""
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(script)
    path.chmod(0o755)


def write_3dflex_manifest(
    *,
    source_manifest: Path,
    trajectory_root: Path,
    states: tuple[int, ...],
) -> Path:
    manifest = json.loads(source_manifest.read_text())
    trajectory_bench = trajectory_root / "3dflex_generation"
    trajectory_bench.mkdir(parents=True, exist_ok=True)
    manifest["bench_root"] = str(trajectory_bench)
    manifest["trajectory_states"] = list(states)
    manifest["trajectory_source_manifest"] = str(source_manifest)
    out = trajectory_root / "3dflex_trajectory_source_manifest.json"
    out.write_text(json.dumps(manifest, indent=2) + "\n")
    return out


def parse_states(values: list[int] | None) -> tuple[int, ...]:
    if values:
        return tuple(int(x) for x in values)
    return DEFAULT_STATES


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--source-root", type=Path, default=DEFAULT_SOURCE_ROOT)
    parser.add_argument("--bench-root", type=Path, default=DEFAULT_BENCH_ROOT)
    parser.add_argument("--rebase-repo", type=Path, default=DEFAULT_REBASE_REPO)
    parser.add_argument("--n-images", type=int, default=300000)
    parser.add_argument("--states", type=int, nargs="+", default=None)
    parser.add_argument("--trajectory-root", type=Path, default=None)
    args = parser.parse_args()

    states = parse_states(args.states)
    label = n_label(args.n_images)
    source_run = (
        args.source_root.resolve() / label / "runs" / f"{label}_seed0000"
    )
    bench_root = args.bench_root.resolve()
    trajectory_root = (
        args.trajectory_root.resolve()
        if args.trajectory_root
        else bench_root / f"state_trajectory_{label}_20260601"
    )
    pipeline_dir = (
        source_run / "06_pipeline_true_recovar_h100_fullmem_movingfocus_20260531"
    )
    recovar_target_dir = trajectory_root / "recovar" / "targets_zdim4_noreg_state_means"
    recovar_sbatch = trajectory_root / "recovar_compute_state_trajectory.sbatch"
    flex_source_manifest = (
        bench_root
        / "cryosparc_3dflex"
        / f"cryosparc_3dflex_noise3_b80_{label}_corrected_k1_jobs.json"
    )

    for path in (source_run, pipeline_dir, flex_source_manifest):
        if not path.exists():
            raise FileNotFoundError(path)

    gt_dir = trajectory_root / "gt"
    drgn_dir = trajectory_root / "cryodrgn"
    cryodrgn_source = (
        bench_root
        / label
        / "evaluation"
        / "cryodrgn"
        / "zdim1"
        / "decoded_volumes"
        / "labels_mean_z_epoch019"
    )
    for state in states:
        symlink_replace(source_run / "04_ground_truth" / f"gt_vol{state:04d}.mrc", gt_dir / f"state{state:04d}.mrc")
        drgn_map = cryodrgn_source / f"gt_label_{state:03d}.mrc"
        if drgn_map.exists():
            symlink_replace(drgn_map, drgn_dir / f"state{state:04d}.mrc")

    target_manifest = write_state_targets(
        source_run=source_run,
        pipeline_dir=pipeline_dir,
        states=states,
        target_dir=recovar_target_dir,
    )
    write_recovar_sbatch(
        path=recovar_sbatch,
        rebase_repo=args.rebase_repo.resolve(),
        source_run=source_run,
        pipeline_dir=pipeline_dir,
        target_dir=recovar_target_dir,
        output_dir=trajectory_root,
        states=states,
    )
    flex_manifest = write_3dflex_manifest(
        source_manifest=flex_source_manifest,
        trajectory_root=trajectory_root,
        states=states,
    )

    submit_commands = trajectory_root / "submit_commands.txt"
    state_args = " ".join(str(x) for x in states)
    submit_commands.write_text(
        "\n".join(
            [
                f"sbatch {recovar_sbatch}",
                (
                    "module purge && module load cryosparc-tools/5.0.3 && "
                    f"python {args.rebase_repo.resolve()}/scripts/experiments/"
                    "spike_fullatom_method_benchmark/"
                    "submit_cryosparc_3dflex_generate_from_manifest.py "
                    f"--manifest {flex_manifest} --project-dir "
                    "/tigress/CRYOEM/singerlab/mg6942/CS-testres "
                    f"--states {state_args} --submit --overwrite"
                ),
            ]
        )
        + "\n"
    )

    summary = {
        "trajectory_root": str(trajectory_root),
        "states": list(states),
        "source_run": str(source_run),
        "cryodrgn_source": str(cryodrgn_source),
        "gt_dir": str(gt_dir),
        "cryodrgn_dir": str(drgn_dir),
        "recovar_target_manifest": target_manifest,
        "recovar_sbatch": str(recovar_sbatch),
        "3dflex_source_manifest": str(flex_manifest),
        "submit_commands": str(submit_commands),
    }
    (trajectory_root / "manifest.json").write_text(json.dumps(summary, indent=2) + "\n")
    print(json.dumps(summary, indent=2))


if __name__ == "__main__":
    main()
